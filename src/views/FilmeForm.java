    private boolean checkFieldEmpty () {
        String textTitulo = tfTitulo.getText();  
        String textAno = tfAno.getText();
        String textDiretor = tfDiretor.getText();
        String textPais = tfPais.getText();
        
        if (cbGeneros.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um Genero", "Atenção", JOptionPane.WARNING_MESSAGE);            
            return true;
        }
        
        if (textTitulo.length() < 1) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Título", "Atenção", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        
        if (textAno.length() < 1) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Ano", "Atenção", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        
        if (textDiretor.length() < 1) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Diretor", "Atenção", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        
        if (textPais.length() < 1) {
            JOptionPane.showMessageDialog(this, "Preencha o campo País", "Atenção", JOptionPane.WARNING_MESSAGE);
            return true;
        }        
               
        return false;
    }

