    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (checkFieldEmpty()) return ;

        Filme filme = null;
        Genero genero = null;

        if (regNew) {
            filme = new Filme();
        } else {
            filme = (Filme) cbFilmes.getSelectedItem();
        }
        
        genero = (Genero) cbGeneros.getSelectedItem();
        
        filme.setTitulo(tfTitulo.getText());
        filme.setAno(tfAno.getText());
        filme.setDiretor(tfDiretor.getText());
        filme.setPais(tfPais.getText());        
        filme.setGenero(genero.getIdGenero());                

        if (regNew) {
            cbFilmes.addItem(filme);
            listFilme.add(filme);
            repository.addFilme(filme);
        } else {
            repository.updateFilme(filme);
        }

        

        btNovo.setEnabled(true);
        btSalvar.setEnabled(false);
        btNovoActionPerformed(null);
        JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btSalvarActionPerformed

    private void cbFilmesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbFilmesItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            Filme filme = (Filme) cbFilmes.getSelectedItem();
            
            tfTitulo.setText(filme.getTitulo());
            tfAno.setText(filme.getAno());
            tfDiretor.setText(filme.getDiretor());
            tfPais.setText(filme.getPais());
            
            Genero genero = repository.findGenero(filme.getGenero());
            
            if (genero != null) {
                cbGeneros.getModel().setSelectedItem(genero);
                for (int i = 0; i < cbGeneros.getModel().getSize(); i++) {                    
                    final String txt = genero.getIdGenero() + " - " + genero.getDescricao();
                    if (cbGeneros.getModel().getElementAt(i).toString().equals(txt)) {
                        cbGeneros.setSelectedIndex(i);
                    }
                }        
            }
            
            btSalvar.setEnabled(true);
            btNovo.setEnabled(true);
            
            regNew = false;
        }
    }//GEN-LAST:event_cbFilmesItemStateChanged

