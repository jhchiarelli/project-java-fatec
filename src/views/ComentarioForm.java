    private boolean checkFieldEmpty () {
        String textComentarios = tfComentario.getText();     
        String textNota = tfNota.getText();
        String textUsuario = tfNota.getText();
        
        if (cbFilmes.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um Filme", "Atenção", JOptionPane.WARNING_MESSAGE);            
            return true;
        }
        
        if (textComentarios.length() < 1) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Comentário", "Atenção", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        
        if (textNota.length() < 1) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Nota", "Atenção", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        
        if (textUsuario.length() < 1) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Usuário", "Atenção", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        
               
        return false;
    }

