    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (checkFieldEmpty()) return ;

        Comentario comentario = null;
        Filme filme = null;

        if (regNew) {
            comentario = new Comentario();
        } else {
            comentario = (Comentario) cbComentarios.getSelectedItem();
        }

        filme = (Filme) cbFilmes.getSelectedItem();

        comentario.setNota(Double.parseDouble(tfNota.getText()));
        comentario.setUsuario(tfUsuario.getText());
        comentario.setComentario(tfComentario.getText());
        comentario.setFilme(filme.getIdFilme());

        if (regNew) {
            cbComentarios.addItem(comentario);
            listComentario.add(comentario);
            repository.addComentario(comentario);
        } else {
            repository.updateComentario(comentario);
        }

        btNovo.setEnabled(true);
        btSalvar.setEnabled(false);
        btNovoActionPerformed(null);
        JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btSalvarActionPerformed

    private void cbComentariosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbComentariosItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            Comentario comentario = (Comentario) cbComentarios.getSelectedItem();

            tfNota.setText(comentario.getNota().toString());
            tfUsuario.setText(comentario.getUsuario());
            tfComentario.setText(comentario.getComentario());

            Filme filme = repository.findFilme(comentario.getFilme());

            if (filme != null) {
                cbFilmes.getModel().setSelectedItem(filme);
                for (int i = 0; i < cbFilmes.getModel().getSize(); i++) {                    
                    final String txt = filme.getIdFilme()+ " - " + filme.getTitulo();
                    if (cbFilmes.getModel().getElementAt(i).toString().equals(txt)) {
                        cbFilmes.setSelectedIndex(i);
                    }
                }   
            }

            btSalvar.setEnabled(true);
            btNovo.setEnabled(true);

            regNew = false;
        }
    }//GEN-LAST:event_cbComentariosItemStateChanged

